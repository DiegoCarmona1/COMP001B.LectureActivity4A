// if, if--else, else statement

let myGrade = 90;

if (myGrade >= 90) {
    console.log("A")
} else if (myGrade >= 80) {
    console.log("B");
} else if (myGrade >= 70) {
    console.log("C");
} else if (myGrade >= 60) {
    console.log("D");
} else {
    console.log("F");
}


//switch
switch (myGrade) {
    case 90: console.log("A")
        break;
    case 80: console.log("B")
        break;
}